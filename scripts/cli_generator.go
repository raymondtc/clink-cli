// CLICommand represents a CLI command generated from OpenAPI operation
type CLICommand struct {
	Name        string
	Use         string
	Short       string
	Long        string
	HTTPMethod  string
	Path        string
	Flags       []CLIFlag
	Args        []CLIArg
	HasBody     bool
	BodyFields  []BodyField
}

// CLIFlag represents a command flag
type CLIFlag struct {
	Name        string
	Shorthand   string
	Type        string
	Default     string
	Description string
	Required    bool
}

// CLIArg represents a positional argument
type CLIArg struct {
	Name     string
	Type     string
	Required bool
}

// BodyField represents a field in request body
type BodyField struct {
	Name     string
	Type     string
	Required bool
}

// CLIGenerator generates CLI code from OpenAPI spec
type CLIGenerator struct {
	Spec      OpenAPI
	Commands  []CLICommand
}

// NewCLIGenerator creates a new CLI generator
func NewCLIGenerator(spec OpenAPI) *CLIGenerator {
	return &CLIGenerator{
		Spec:     spec,
		Commands: extractCommands(spec),
	}
}

// extractCommands extracts CLI commands from OpenAPI spec
func extractCommands(spec OpenAPI) []CLICommand {
	var commands []CLICommand

	// Group operations by tag
	operationsByTag := make(map[string][]struct {
		Method string
		Path   string
		Op     *Operation
	})

	for path, item := range spec.Paths {
		if item.Get != nil {
			tag := getTag(item.Get.Tags)
			operationsByTag[tag] = append(operationsByTag[tag], struct {
				Method string
				Path   string
				Op     *Operation
			}{"GET", path, item.Get})
		}
		if item.Post != nil {
			tag := getTag(item.Post.Tags)
			operationsByTag[tag] = append(operationsByTag[tag], struct {
				Method string
				Path   string
				Op     *Operation
			}{"POST", path, item.Post})
		}
	}

	// Create commands for each tag
	for tag, ops := range operationsByTag {
		cmd := CLICommand{
			Name:   commandName(tag),
			Use:    commandName(tag),
			Short:  tag + " operations",
		}

		// Add subcommands for each operation
		for _, op := range ops {
			subcmd := createSubcommand(op.Method, op.Path, op.Op)
			// Merge flags and args from subcommands
			// For now, we create separate commands
			_ = subcmd
		}

		commands = append(commands, cmd)
	}

	return commands
}

// createSubcommand creates a subcommand from operation
func createSubcommand(method, path string, op *Operation) CLICommand {
	cmd := CLICommand{
		Name:       commandName(op.OperationID),
		Use:        commandName(op.OperationID),
		Short:      op.Summary,
		Long:       op.Summary,
		HTTPMethod: method,
		Path:       path,
	}

	// Extract flags from parameters
	for _, param := range op.Parameters {
		flag := CLIFlag{
			Name:        flagName(param.Name),
			Type:        schemaToFlagType(param.Schema),
			Description: param.Description,
			Required:    param.Required,
		}
		cmd.Flags = append(cmd.Flags, flag)
	}

	// Extract body fields
	if op.RequestBody != nil {
		cmd.HasBody = true
		for contentType, media := range op.RequestBody.Content {
			_ = contentType
			if media.Schema.Properties != nil {
				for name, schema := range media.Schema.Properties {
					field := BodyField{
						Name: flagName(name),
						Type: schemaToFlagType(schema),
					}
					// Check if required
					for _, req := range media.Schema.Required {
						if req == name {
							field.Required = true
							break
						}
					}
					cmd.BodyFields = append(cmd.BodyFields, field)
				}
			}
		}
	}

	return cmd
}

// Generate generates CLI code
func (g *CLIGenerator) Generate(outputPath string) error {
	data := struct {
		Commands []CLICommand
	}{
		Commands: g.Commands,
	}

	return generateFile(outputPath, cliMainTemplate, data)
}

// Helper functions
func getTag(tags []string) string {
	if len(tags) > 0 {
		return tags[0]
	}
	return "default"
}

func commandName(s string) string {
	// Convert operationId to command name
	// listCdrIbs -> list, describeClient -> describe
	s = strings.ToLower(s)
	s = strings.ReplaceAll(s, "_", "-")
	return s
}

func flagName(s string) string {
	// Convert param name to flag name
	// startTime -> start-time
	var result strings.Builder
	for i, r := range s {
		if i > 0 && r >= 'A' && r <= 'Z' {
			result.WriteByte('-')
			result.WriteByte(byte(r - 'A' + 'a'))
		} else {
			result.WriteByte(byte(r))
		}
	}
	return result.String()
}

func schemaToFlagType(schema Schema) string {
	switch schema.Type {
	case "string":
		return "string"
	case "integer":
		return "int"
	case "boolean":
		return "bool"
	default:
		return "string"
	}
}

