// generateFile generates a file from template
func generateFile(filename, tmplStr string, data interface{}) error {
	tmpl, err := template.New(filepath.Base(filename)).Parse(tmplStr)
	if err != nil {
		return fmt.Errorf("parse template: %w", err)
	}

	file, err := os.Create(filename)
	if err != nil {
		return fmt.Errorf("create file: %w", err)
	}
	defer file.Close()

	if err := tmpl.Execute(file, data); err != nil {
		return fmt.Errorf("execute template: %w", err)
	}

	return nil
}

// GenerateCLICommands generates full CLI commands from OpenAPI spec
func GenerateCLICommands(specPath string) error {
	data, err := os.ReadFile(specPath)
	if err != nil {
		return fmt.Errorf("read spec: %w", err)
	}

	var spec OpenAPI
	if err := yaml.Unmarshal(data, &spec); err != nil {
		return fmt.Errorf("parse spec: %w", err)
	}

	// Generate command files
	commands := []struct {
		Tag  string
		Name string
	}{
		{"CallRecords", "records"},
		{"AgentStatus", "agents"},
		{"CallControl", "call"},
		{"Queue", "queue"},
	}

	for _, cmd := range commands {
		if err := generateCommandFile(spec, cmd.Tag, cmd.Name); err != nil {
			return fmt.Errorf("generate %s: %w", cmd.Name, err)
		}
	}

	return nil
}

// generateCommandFile generates a single command file
func generateCommandFile(spec OpenAPI, tag, name string) error {
	data := CommandFileData{
		Package: "main",
		Name:    name,
	}

	// Find operations for this tag
	for path, item := range spec.Paths {
		if item.Get != nil && hasTag(item.Get.Tags, tag) {
			op := OperationInfo{
				Name:       operationToCmdName(item.Get.OperationID),
				HTTPMethod: "GET",
				Path:       path,
				Summary:    item.Get.Summary,
			}
			for _, p := range item.Get.Parameters {
				op.Flags = append(op.Flags, FlagInfo{
					Name:        flagName(p.Name),
					Type:        schemaToFlagType(p.Schema),
					Description: p.Description,
					Required:    p.Required,
				})
			}
			data.Operations = append(data.Operations, op)
		}
		if item.Post != nil && hasTag(item.Post.Tags, tag) {
			op := OperationInfo{
				Name:       operationToCmdName(item.Post.OperationID),
				HTTPMethod: "POST",
				Path:       path,
				Summary:    item.Post.Summary,
			}
			if item.Post.RequestBody != nil {
				for contentType, media := range item.Post.RequestBody.Content {
					_ = contentType
					if media.Schema.Properties != nil {
						for fname, schema := range media.Schema.Properties {
							op.Flags = append(op.Flags, FlagInfo{
								Name:        flagName(fname),
								Type:        schemaToFlagType(schema),
								Description: fname,
								Required:    isRequired(fname, media.Schema.Required),
							})
						}
					}
				}
			}
			data.Operations = append(data.Operations, op)
		}
	}

	filename := fmt.Sprintf("cmd/clink/%s_gen.go", name)
	return generateFileFromTemplate(filename, commandFileTemplate, data)
}

// CommandFileData holds data for command file template
type CommandFileData struct {
	Package    string
	Name       string
	Operations []OperationInfo
}

// OperationInfo holds operation info
type OperationInfo struct {
	Name       string
	HTTPMethod string
	Path       string
	Summary    string
	Flags      []FlagInfo
}

// FlagInfo holds flag info
type FlagInfo struct {
	Name        string
	Type        string
	Description string
	Required    bool
	Default     string
}

// operationToCmdName converts operationId to command name
func operationToCmdName(opID string) string {
	// listCdrIbs -> list
	// getAgentStatus -> status
	parts := []string{
		"list", "get", "describe", "create", "update", "delete",
		"online", "offline", "pause", "unpause", "make", "webcall",
	}

	lower := strings.ToLower(opID)
	for _, p := range parts {
		if strings.HasPrefix(lower, p) {
			return p
		}
	}

	// Default: use first word
	for i, r := range opID {
		if i > 0 && r >= 'A' && r <= 'Z' {
			return strings.ToLower(opID[:i])
		}
	}
	return strings.ToLower(opID)
}

func hasTag(tags []string, tag string) bool {
	for _, t := range tags {
		if t == tag {
			return true
		}
	}
	return false
}

func isRequired(field string, required []string) bool {
	for _, r := range required {
		if r == field {
			return true
		}
	}
	return false
}

