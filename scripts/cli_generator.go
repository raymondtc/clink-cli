func generateFileFromTemplate(filename, tmplStr string, data interface{}) error {
	funcMap := template.FuncMap{
		"title": strings.Title,
		"flagTypeToFunc": func(t string) string {
			switch t {
			case "int":
				return "Int"
			case "bool":
				return "Bool"
			default:
				return "String"
			}
		},
	}

	tmpl, err := template.New(filepath.Base(filename)).Funcs(funcMap).Parse(tmplStr)
	if err != nil {
		return fmt.Errorf("parse template: %w", err)
	}

	file, err := os.Create(filename)
	if err != nil {
		return fmt.Errorf("create file: %w", err)
	}
	defer file.Close()

	if err := tmpl.Execute(file, data); err != nil {
		return fmt.Errorf("execute template: %w", err)
	}

	return nil
}
